package MezziDiTrasporto;

public class Ruote {

    /**
     * Default constructor
     */
    public Ruote() {
    }

 
    private int numero;
    private  String materiale;
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public String getMateriale() {
		return materiale;
	}
	public void setMateriale(String materiale) {
		this.materiale = materiale;
	}


}